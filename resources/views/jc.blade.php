        <br>
        <h4 class=''><b>Rheumatoid arthritis</b><br> This is an inflammatory form of arthritis. The synovial membrane (synovium) is attacked. By this, the joint lining is affected. The inflammation affects not only the joints, but also other parts of body causing excessive pain and tiredness.
            </h4>
        <br>
        <h4 class=''><b>Infectious arthritis (septic arthritis)</b><br> Infectious arthritis spreads when there is infection in the synovial fluid and tissues of a joint. It is usually caused by bacteria, but could also be caused by fungi or viruses. Bacteria, fungi or viruses may spread, and infect the joint. Most susceptible people are those who already have some form of arthritis and develop an infection that travels in the bloodstream.
